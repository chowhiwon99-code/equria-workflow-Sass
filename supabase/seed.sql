# 세금계산서 처리 절차
1. 거래 구분 확인: 과세 / 영세율(수출 등) / 면세
2. 공급가액 확정 → 부가세 = 공급가액 × 10%(영세율 0, 면세 없음) → 합계 = 공급가액 + 부가세
3. 필수 기재사항 점검: 공급자·공급받는자 사업자번호/상호/대표/주소, 작성일자, 공급가액, 세액, 품목, 비고
4. 전자세금계산서 발행 절차 안내(홈택스 또는 연동 솔루션, 발행 후 익일까지 전송)
5. 오류 시: 수정세금계산서 사유(기재착오·환입·계약해제·공급가액 변동 등) 안내

